// Types
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

export interface FounderIntake {
  id: string;
  user_id: string | null;
  org_profile_id: string | null;
  idea_summary: string | null;
  problem_statement: string | null;
  target_beneficiary: Record<string, unknown> | null;
  issue_areas: string[] | null;
  geographic_focus: Record<string, unknown> | null;
  founder_motivation: string | null;
  existing_orgs_shown: unknown[] | null;
  differentiation: string | null;
  partnership_decision: string | null;
  recommended_entity_type: string | null;
  entity_type_rationale: string | null;
  entity_type_scores: unknown[] | null;
  revenue_model: string[] | null;
  matched_alma_interventions: unknown[] | null;
  evidence_gaps: string[] | null;
  matched_grants: unknown[] | null;
  matched_foundations: unknown[] | null;
  procurement_pathways: unknown[] | null;
  action_plan: unknown[] | null;
  project_brief_md: string | null;
  draft_email: string | null;
  phase: string;
  status: string;
  metadata: Record<string, unknown>;
  created_at: string;
  updated_at: string;
}

export interface IntakeMessage {
  id: string;
  intake_id: string;
  role: 'user' | 'assistant' | 'system';
  content: string;
  phase: string | null;
  metadata: Record<string, unknown>;
  created_at: string;
}

// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// CRUD
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

export async function createIntake(): Promise<FounderIntake | null> {
  const supabase = getServiceSupabase();
  const { data, error } = await supabase
    .from('founder_intakes')
    .insert({})
    .select()
    .single();
  if (error) return null;
  return data as FounderIntake;
}

export async function getIntake(id: string): Promise<FounderIntake | null> {
  const supabase = getServiceSupabase();
  const { data, error } = await supabase
    .from('founder_intakes')
    .select('*')
    .eq('id', id)
    .maybeSingle();
  if (error || !data) return null;
  return data as FounderIntake;
}

export async function updateIntake(
  id: string,
  updates: Partial<FounderIntake>,
): Promise<FounderIntake | null> {
  const supabase = getServiceSupabase();
  const { data, error } = await supabase
    .from('founder_intakes')
    .update({ ...updates, updated_at: new Date().toISOString() })
    .eq('id', id)
    .select()
    .single();
  if (error) return null;
  return data as FounderIntake;
}

// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
// Messages
// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

export async function getIntakeMessages(intakeId: string): Promise<IntakeMessage[]> {
  const supabase = getServiceSupabase();
  const { data } = await supabase
    .from('founder_intake_messages')
    .select('*')
    .eq('intake_id', intakeId)
    .order('created_at');
  return (data ?? []) as IntakeMessage[];
}

export async function addIntakeMessage(
  intakeId: string,
  role: 'user' | 'assistant' | 'system',
  content: string,
  phase?: string,
): Promise<IntakeMessage | null> {
  const supabase = getServiceSupabase();
  const { data, error } = await supabase
    .from('founder_intake_messages')
    .insert({ intake_id: intakeId, role, content, phase })
    .select()
    .single();
  if (error) return null;
  return data as IntakeMessage;
}

